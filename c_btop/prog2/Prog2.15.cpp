/*Program 2.15 Character constants and printf*/
#include <stdio.h>

int main(void)
{
    char first = 'T';
    char second = 20;

    printf("\n The first example as a letter looks like this - %c", first);
    printf("\n The first example as a number looks like this - %d", first);
    printf("\n The second example as a letter looks like this - %c", second);
    printf("\n The second example as a number looks like this - %d\n", second);

    return 0;
}
